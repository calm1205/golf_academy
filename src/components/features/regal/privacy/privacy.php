    ・当社が利用目的の達成に必要な範囲内において、個人情報の取扱いの全部または一部を委託する場合<br>
    ・合併その他の事由による事業の承継に伴って個人情報が提供される場合<br>
    ・個人情報を特定の者との間で共同して利用する場合であって、共同して利用される個人情報の項目、共同して利用する者の範囲、利用する者の利益目的及び当該個人情報の管理について責任を有する者の氏名または名称について、あらかじめ本人に通知し、また・は本人が容易に知り得る状態であった場合<br><br>

