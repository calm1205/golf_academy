    当社は、お客様ご本人から個人情報が利用目的の範囲を超えて取り扱われているという理由、または不正の手段により取得されたものであるという理由により、その利用の停止または消去を求められた場合には、遅滞なく必要な調査を行います。<br>
    前項の調査結果に基づき、その請求に応じる必要があると判断した場合には、遅滞なく、当該個人情報の利用停止等を行います。<br>
    当社は、前項の規定に基づき利用停止等を行った場合、または利用停止等を行わない旨の決定をしたときには、遅滞なく、これをお客様に通知致します。<br>
