    当社は、本人から個人情報の開示を求められたときは、本人に対し、遅滞なくこれを開示ます。ただし、開示することによりいずれかに該当する場合は、その全部または一部を開示し、開示しない決定をした場合には、その旨を遅滞なく通知します。なお、個人情報の開示に際しましては、１件あたり1,000円の手数料を申し受けます。<br>
    ・本人または第三者の生命、身体、財産その他の権利利益を害するおそれがある場合<br>
    ・当社の業務の適正な実施に著しい支障を及ぼすおそれがある場合<br>
    ・その他、法令に違反することとなる場合<br>
    前項の定めにかかわらず、履歴情報及び特性情報などの個人情報以外の情報については、原則として開示致しません。<br><br>

    <span>第6条（個人情報の訂正および削除）</span><br>
    お客様は、当社の保有する事故の個人情報が誤った情報である場合には、当社が定める手続きにより、当社に対して個人情報の訂正、追加または削除を訂正することができます。<br>
    当社はお客様から前項の請求を受けて、その請求に応じる必要があると判断した場合には、遅滞なく、当該個人情報の訂正を行うものとします。<br>
    当社は前項の規定に基づき、訂正等を行った場合、または訂正等を行わない旨を決定したときには、遅滞なく、お客様に通知致します。<br><br>

